const I18nContext = createContext({
  language: "en",
  setLanguage: () => {},
  t: (value) => value,
});

function getInitialLanguage() {
  const saved = window.localStorage.getItem(STORAGE_KEY);
  if (saved && SUPPORTED_LANGUAGES.includes(saved)) {
    return saved;
  }
  return "en";
}

function getInitialAutoTranslationCache() {
  try {
    const raw = window.localStorage.getItem(AUTO_TRANSLATION_CACHE_KEY);
    if (!raw) {
      return {};
    }

    const parsed = JSON.parse(raw);
    return parsed && typeof parsed === "object" ? parsed : {};
  } catch {
    return {};
  }
}

export function I18nProvider({ children }) {
  const [language, setLanguageState] = useState(getInitialLanguage);
  const [autoTranslationCache, setAutoTranslationCache] = useState(
    getInitialAutoTranslationCache,
  );
  const pendingTranslationsRef = useRef(new Set());

  const setLanguage = (nextLanguage) => {
    const normalized = SUPPORTED_LANGUAGES.includes(nextLanguage) ? nextLanguage : "en";
    window.localStorage.setItem(STORAGE_KEY, normalized);
    setLanguageState(normalized);
  };

  const requestAutoTranslation = useCallback(
    async (text) => {
      if (
        language === "en" ||
        !text ||
        typeof text !== "string" ||
        import.meta.env.MODE === "test" ||
        !/[A-Za-z]/.test(text)
      ) {
        return;
      }

      const requestKey = `${language}::${text}`;

      if (pendingTranslationsRef.current.has(requestKey)) {
        return;
      }

      pendingTranslationsRef.current.add(requestKey);

      try {
        const response = await fetch(
          `/api/translate?target=${encodeURIComponent(language)}&text=${encodeURIComponent(text)}`,
        );

        if (!response.ok) {
          return;
        }

        const data = await response.json();
        const translatedText = typeof data?.translatedText === "string" ? data.translatedText : "";

        if (!translatedText) {
          return;
        }

        setAutoTranslationCache((currentCache) => {
          const languageCache = currentCache[language] || {};

          if (languageCache[text] === translatedText) {
            return currentCache;
          }

          const nextCache = {
            ...currentCache,
            [language]: {
              ...languageCache,
              [text]: translatedText,
            },
          };

          window.localStorage.setItem(AUTO_TRANSLATION_CACHE_KEY, JSON.stringify(nextCache));
          return nextCache;
        });
      } catch {
        // Silent fallback: keep original text if translation cannot be fetched.
      } finally {
        pendingTranslationsRef.current.delete(requestKey);
      }
    },
    [language],
  );

  const value = useMemo(() => {
    const dictionary = TRANSLATIONS[language] || {};
    const languageCache = autoTranslationCache[language] || {};

    const t = (text) => {
      if (typeof text !== "string" || !text) {
        return text;
      }

      if (dictionary[text]) {
        return dictionary[text];
      }

      if (languageCache[text]) {
        return languageCache[text];
      }

      requestAutoTranslation(text);
      return text;
    };

    return { language, setLanguage, t };
  }, [language, autoTranslationCache, requestAutoTranslation]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  return useContext(I18nContext);
}
